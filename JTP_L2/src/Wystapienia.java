public class Wystapienia
{
    private String imie;
    private int ilosc;

    public Wystapienia(String imie, int ilosc)
    {
        this.imie = imie;
        this.ilosc = ilosc;
    }

    public int getIlosc()
    {
        return ilosc;
    }

    public String getImie()
    {
        return imie;
    }
}
